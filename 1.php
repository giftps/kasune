										<h2>Education - The Mayeba Fund</h2>
										<p><span style="color: green;">T</span>he<span style="color: green;">K</span>asune<span style="color: green;">P</span>roject provides access to higher education by providing funding using an equitable approach.&nbsp;&nbsp;How funding is provided depends on the ambitions of the young person, their basic formal education, school leaving results etc. Prospective students are then supported in making goals that are realistic and achievable. <span style="color: green;">T</span>he<span style="color: green;">K</span>asune<span style="color: green;">P</span>roject supports academic or vocational education as it is important that opportunities are reflective of different abilities and ambitions. <span style="color: green;">T</span>he<span style="color: green;">K</span>asune<span style="color: green;">P</span>roject encourages families to participate on all levels in supporting their loved one (including financial support) on the agreed educational journey.</p>
										<p> <span style="color: green;">T</span>he<span style="color: green;">K</span>asune<span style="color: green;">P</span>roject also provides funding for extra lessons in preparation for Grade 7 and 9 GCSE exams respectively. The Grade 7 certificate is the primary school leaving certificate, while the Grade 9 certificate enables young people to continue their secondary school education. Without obtaining the required level of points needed at each stage of key exams, the educational journey for many young people ends at either 13 or 16 years of age respectfully. Additionally, <span style="color: green;">T</span>he<span style="color: green;">K</span>asune<span style="color: green;">P</span>roject pays for extra tuition in preparation for Grade 12 GCSE exams, for 18 year olds. These are even more crucial as young people have come this far and they cannot afford to leave secondary school without a full Grade 12 GCSE Certificate. An interruption at any stage of this educational journey, could be a catalyst for early marriages, unplanned pregnancies, mental health issues and a myriad of forms of substance abuse, all this, already now painfully commonplace.</p>
